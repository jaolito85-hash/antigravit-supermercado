-- 1. Criar tabela de feedbacks
--    Aqui ficam todas as mensagens recebidas pelo WhatsApp
CREATE TABLE IF NOT EXISTS feedbacks (
    id bigint generated by default as identity primary key,
    sender text,
    name text,
    message text not null,
    timestamp timestamp with time zone default timezone('utc'::text, now()),
    category text,
    region text,
    urgency text,
    sentiment text,
    topic text,
    loja text default 'Matriz',
    status text default 'aberto',
    resolved_at text,
    created_at timestamp with time zone default timezone('utc'::text, now())
);

-- 2. Criar tabela de config
--    Guarda categorias, regiões e as promoções do dia e da semana
CREATE TABLE IF NOT EXISTS config (
    id bigint generated by default as identity primary key,
    type text not null,
    name text not null,
    color text,
    count int default 0,
    created_at timestamp with time zone default timezone('utc'::text, now())
);

-- 3. Criar tabela de produtos
--    Por enquanto não usada ativamente, mas necessária para o sistema funcionar
CREATE TABLE IF NOT EXISTS produtos (
    id bigint generated by default as identity primary key,
    nome text not null,
    categoria text,
    preco numeric(10, 2) not null,
    preco_promo numeric(10, 2),
    em_estoque boolean default true,
    loja text default 'Matriz',
    unidade text default 'un',
    data_atualizacao timestamp with time zone default timezone('utc'::text, now()),
    created_at timestamp with time zone default timezone('utc'::text, now())
);

-- 4. Criar tabela de lista de espera
CREATE TABLE IF NOT EXISTS lista_espera (
    id bigint generated by default as identity primary key,
    sender text not null,
    name text,
    produto_nome text not null,
    produto_id bigint references produtos(id),
    loja text default 'Matriz',
    notificado boolean default false,
    created_at timestamp with time zone default timezone('utc'::text, now())
);

-- 5. Criar tabela de notificações opt-in
CREATE TABLE IF NOT EXISTS notificacoes (
    id bigint generated by default as identity primary key,
    sender text not null,
    name text,
    tipo text not null,
    -- 'ofertas', 'receita'
    ativo boolean default true,
    created_at timestamp with time zone default timezone('utc'::text, now())
);

