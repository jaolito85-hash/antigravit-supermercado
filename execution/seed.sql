-- ============================================
-- 9. Habilitar RLS (Row Level Security)
--    Segurança: apenas quem tem a chave pode acessar
-- ============================================
ALTER TABLE feedbacks    ENABLE ROW LEVEL SECURITY;
ALTER TABLE config       ENABLE ROW LEVEL SECURITY;
ALTER TABLE produtos     ENABLE ROW LEVEL SECURITY;
ALTER TABLE lista_espera ENABLE ROW LEVEL SECURITY;
ALTER TABLE notificacoes ENABLE ROW LEVEL SECURITY;

-- ============================================
-- 10. Criar políticas de acesso (anon key)
-- ============================================
CREATE POLICY "Allow public read feedbacks"    ON feedbacks    FOR SELECT USING (true);
CREATE POLICY "Allow public insert feedbacks"  ON feedbacks    FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public update feedbacks"  ON feedbacks    FOR UPDATE USING (true);

CREATE POLICY "Allow public read config"       ON config       FOR SELECT USING (true);
CREATE POLICY "Allow public insert config"     ON config       FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public update config"     ON config       FOR UPDATE USING (true);
CREATE POLICY "Allow public delete config"     ON config       FOR DELETE USING (true);

CREATE POLICY "Allow public read produtos"     ON produtos     FOR SELECT USING (true);
CREATE POLICY "Allow public insert produtos"   ON produtos     FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public update produtos"   ON produtos     FOR UPDATE USING (true);

CREATE POLICY "Allow public read lista_espera" ON lista_espera FOR SELECT USING (true);
CREATE POLICY "Allow public insert lista_espera" ON lista_espera FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public update lista_espera" ON lista_espera FOR UPDATE USING (true);

CREATE POLICY "Allow public read notificacoes"   ON notificacoes FOR SELECT USING (true);
CREATE POLICY "Allow public insert notificacoes" ON notificacoes FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public update notificacoes" ON notificacoes FOR UPDATE USING (true);

